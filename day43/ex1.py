s = "python seems to be interesting"
m = "I use it in AI/ML"
print(len(s)) #len()-total characters of string
print(s.upper()) #upper()-converts all char to uppercase
print(s.lower())#lower()-converts all to lower case
print(s.strip())#remove spaces and trailing 
print(s.replace("fun", "awesome"))#replaces the unwanted ones.
print(s + " " + m) #concatenate str +
print(m * 3) #repeat string



