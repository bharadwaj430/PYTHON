"""
What is the difference between single, double, and triple quotes in Python strings?

Single (') and double (") quotes can be used interchangeably for creating strings.
 The choice between them often depends on the need to include quotes of one type inside the string without escaping them.

 Triple quotes allow for multiline strings and are commonly used for docstrings (function or module documentation).

"""
single_quote_string = 'hi everyone.'
print(single_quote_string)

double_quote_string = "done with today's work."
print(double_quote_string)

triple_quote_string = '''Calculated risk leads to success'''
print(triple_quote_string)
